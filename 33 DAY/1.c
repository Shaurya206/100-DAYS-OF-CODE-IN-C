//Search in a sorted array using binary search.

#include<stdio.h>
int main(){

    int size;

    printf("enter the size of array: ");      //input to declare the array size
    scanf("%d",&size);

    int array[size];

    for(int i = 0 ; i < size ; i++){           //loop to store value in array
        printf("enter the %d value: ",i+1);
        scanf("%d",&array[i]);
    }

    int find;

    printf("enter the number you want to find: ");     //input to find which value you want to find in array
    scanf("%d",&find);

    int low = 0 ;
    int high = size - 1;
    int result = -1;

    while(low <= high){                         //loop to check 
        int mid = (low + high)/2;  
        if(array[mid]==find){                 //condition if the value is in the middle f the array
        result = mid;
        break;
    }
    else if(array[mid] < find){
        low = mid + 1;
    }
    else{
        high = mid - 1;
    }
    }

    if(result != -1){
        printf("the the value %d is found at: %d index",find,result);  //output if the result is found
    }
    else{
        printf("the value was not found in the array");    //output if the result is not found
    }

    return 0;
}                                //end